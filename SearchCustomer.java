    private void BtnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSearchActionPerformed

        String input = TextField1custID.getText();

        if (input.charAt(0) != '0' || input.length() != 10) {
            JOptionPane.showMessageDialog(this, "Enter correct Phone number");
        } else {
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();

            int countXS = 0;
            int countS = 0;
            int countM = 0;
            int countL = 0;
            int countXL = 0;
            int countXXL = 0;

            for (int i = 0; i < Fashionshop.list.size(); i++) {
                Order or = Fashionshop.list.get(i);

                if (or.getTelephone().equals(input)) {
                    String size = or.getSizes();
                    int quantity = or.getQts();

                    switch (size) {
                        case "XS":
                            countXS += quantity;
                            break;
                        case "S":
                            countS += quantity;
                            break;
                        case "M":
                            countM += quantity;
                            break;
                        case "L":
                            countL += quantity;
                            break;
                        case "XL":
                            countXL += quantity;
                            break;
                        case "XXL":
                            countXXL += quantity;
                            break;

                    }
                }
            }

            int TotalAmountXS = calculateAmount("XS", countXS);
            int TotalAmountS = calculateAmount("S", countS);
            int TotalAmountM = calculateAmount("M", countM);
            int TotalAmountL = calculateAmount("L", countL);
            int TotalAmountXL = calculateAmount("XL", countXL);
            int TotalAmountXXL = calculateAmount("XXL", countXXL);

            model.setRowCount(0); // Clear existing rows
            model.addRow(new Object[]{"XS", countXS, TotalAmountXS});
            model.addRow(new Object[]{"S", countS, TotalAmountS});
            model.addRow(new Object[]{"M", countM, TotalAmountM});
            model.addRow(new Object[]{"L", countL, TotalAmountL});
            model.addRow(new Object[]{"XL", countXL, TotalAmountXL});
            model.addRow(new Object[]{"XXL", countXXL, TotalAmountXXL});
        }


    }//GEN-LAST:event_BtnSearchActionPerformed

