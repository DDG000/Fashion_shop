    public static int calculateAmount(String size, int qty) {
        int price = 0;
        if (size.equals("XS")) {
            price = 600 * qty;
        } else if (size.equals("S")) {
            price = 800 * qty;
        } else if (size.equals("M")) {
            price = 900 * qty;
        } else if (size.equals("L")) {
            price = 1000 * qty;
        } else if (size.equals("XL")) {
            price = 1100 * qty;
        } else if (size.equals("XXL")) {
            price = 1200 * qty;
        }
        return price;
    }

